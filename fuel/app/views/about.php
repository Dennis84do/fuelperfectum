<h4>What is FuelPHP?</h4>

<p>FuelPHP is a simple, flexible, community driven PHP 5.3 web framework based on the best ideas of other frameworks with a fresh start.</p>

<p>
	The framework was started in late 2010 by <a href="http://dhorrigan.com/" target="_blank">Dan Horrigan</a> then shortly after
	the team grew to include <a href="http://philsturgeon.co.uk/" target="_blank">Phil Sturgeon</a>,
	<a href="http://jelmerschreuder.nl/" target="_blank">Jelmer Schreuder</a> and
	<a href="http://wanwizard.eu/" target="_blank">Harro Verton</a>. Recently, <a href="http://frenky.net/" target="_blank">Frank de Jonge</a>
	joined the team too. The team has decades of PHP experience between them and have all been involved
	with Open-Source projects such as CodeIgniter, <a href="http://pyrocms.com/" target="_blank">PyroCMS</a>,
	<a href="http://www.exitecms.org/" target="_blank">ExiteCMS</a> and DataMapper ORM to name but a few.
</p>

<p>
	July 31st, 2011 was the date of the 1.0 final release, marking FuelPHP as ready to be used for development of new projects and production sites.<br />
	&nbsp;
</p>

<h4>Philosophy</h4>

<p>
	FuelPHP takes a different approach to many frameworks and strives to be community-driven. We've only been going for less than 6 months
	and already had about <a href="http://github.com/fuel/core/contributors" target="_blank">60 developers</a> commit to the source code and even
	more help us with documentation.
</p>

<p>
	In general we believe that anyone using the framework is in an equal position to spot bugs, fix them and send pull requests
	for bugs and new features alike. There is no "us vs them" to this community, just a group of developers sharing a tool and
	collaboratively improving it as we use it more and more.
</p>

<p>Lightweight, load what you need and configuration over convention. No automagic here!<br />&nbsp;</p>

